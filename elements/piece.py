import turtle
import time
import util

def piece(t, color):
    t.speed(100)

    # Contour noir
    util.row_right_squares(t, 4)
    util.row_left_squares(t, 2)
    t.left(90)
    t.forward(20)
    util.walk_along_up_square(t)
    util.row_left_squares(t, 2)
    util.row_right_squares(t, 8)
    util.row_left_squares(t, 2)
    t.left(90)
    t.forward(20)
    util.walk_along_up_square(t)
    util.row_left_squares(t, 2)
    util.row_right_squares(t, 4)
    util.row_left_squares(t, 2)
    t.left(90)
    t.forward(20)
    util.walk_along_up_square(t)
    util.row_left_squares(t, 2)
    util.row_right_squares(t, 8)
    util.row_left_squares(t, 2)
    t.left(90)
    t.forward(20)
    util.row_right_squares(t, 2)

    # Encrage
    t.color(color)
    t.begin_fill()
    util.walk_along_up_square(t)
    t.forward(80)
    util.long_left_diagonal(t)
    t.left(90)
    t.forward(40)
    util.walk_along_up_square(t)
    t.forward(160)
    util.long_left_diagonal(t)
    t.left(90)
    t.forward(40)
    util.walk_along_up_square(t)
    t.forward(80)
    util.long_left_diagonal(t)
    t.left(90)
    t.forward(40)
    util.walk_along_up_square(t)
    t.forward(160)
    util.long_left_diagonal(t)
    t.left(90)
    t.forward(40)
    t.end_fill()

    # Coeur
    t.left(90)
    t.forward(40)
    t.right(90)
    t.forward(20)
    t.color("#000000")
    util.row_right_squares(t, 2)
    t.left(90)
    util.row_right_squares(t, 8)
    t.left(90)
    util.row_right_squares(t, 2)
    t.left(90)
    util.row_right_squares(t, 8)

    time.sleep(5)

t = turtle.Turtle()
piece(t, "#880000")
    