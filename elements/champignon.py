    # Contour noir
    t.down()
    t.right(90)
    util.row_right_squares(t, 2)
    util.left_square(t)
    util.low_traverse(t)
    util.row_right_squares(t, 8)
    util.left_square(t)
    util.low_traverse(t)
    util.row_right_squares(t, 3)
    t.right(90)
    util.row_right_squares(t, 2)
    t.left(90)
    util.row_right_squares(t, 6)
    util.row_left_squares(t, 2)
    t.left(90)
    t.forward(20)
    util.right_square(t)
    util.double_high_traverse(t)
    util.row_left_squares(t, 2)
    util.row_right_squares(t, 7)
    t.backward(20)
    util.row_left_squares(t, 2)
    t.left(90)
    t.forward(20)
    util.right_square(t)
    util.double_high_traverse(t)
