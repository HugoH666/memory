    t.forward(20)
    t.right(90)
    util.low_traverse(t)
    t.right(90)
    util.low_traverse(t)
    t.end_fill()

    # Pois blanc central
    t.up()
    t.left(180)
    t.forward(60)
    t.left(90)
    t.down()
    t.begin_fill()
    t.forward(60)
    t.right(90)
    util.low_traverse(t)
    t.right(90)
    t.forward(60)
    t.right(90)
    util.low_traverse(t)
    t.right(90)
    t.forward(60)
    t.right(90)
    util.low_traverse(t)
    t.right(90)
    t.forward(60)
    t.right(90)
    util.low_traverse(t)
    t.end_fill()

    # Pois blanc supérieur gauche
    t.up()
    t.left(180)
    t.forward(120)
    t.down()
    t.begin_fill()
    t.forward(20)
    util.double_right_linear_traverse(t)
    util.low_traverse(t)
    util.double_right_linear_traverse(t)
    t.forward(20)
    t.right(90)
    util.low_traverse(t)
    t.right(90)
    util.low_traverse(t)
    t.end_fill()

    # Pois blanc inférieur gauche
    t.up()
    t.forward(60)
    t.down()
    t.begin_fill()
    t.forward(40)
    t.right(90)
    util.low_traverse(t)
    t.right(90)
    t.forward(20)
    t.right(90)
    t.forward(80)
    t.right(90)
    t.forward(20)
    t.right(90)
    util.low_traverse(t)
    t.end_fill()